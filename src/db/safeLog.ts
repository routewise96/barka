/**
 * src/db/safeLog.ts — ЧИСТАЯ обёртка «выполни и никогда не кидай».
 *
 * Фундамент defensive-логирования (часть 1): диагностика ошибок сама не должна
 * становиться источником краха. Если БД недоступна (переполнена память, файл
 * заблокирован, миграция не прошла) — логирование молча проглатывает ошибку.
 *
 * Вынесено отдельным чистым модулем (без нативных импортов), чтобы поведение
 * «не кидать при недоступной БД» было headless-тестируемым.
 */

/**
 * Выполняет асинхронную операцию, гарантируя, что исключение НЕ выйдет наружу.
 * Возвращает true при успехе, false если операция бросила (ошибка проглочена).
 * @param label короткая метка для console.warn (только dev-диагностика).
 */
export async function runSafely(label: string, fn: () => Promise<void>): Promise<boolean> {
  try {
    await fn();
    return true;
  } catch (e) {
    // Намеренно тихо: логирование/диагностика не имеет права ронять приложение.
    // typeof-гард: __DEV__ есть в RN, но отсутствует в headless-Node (тесты).
    if (typeof __DEV__ !== 'undefined' && __DEV__) console.warn(`[safeLog] ${label} проглочено:`, e);
    return false;
  }
}
